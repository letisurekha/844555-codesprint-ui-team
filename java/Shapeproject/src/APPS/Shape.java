package APPS;

public interface Shape {
	public void area();
	public void perimeter();

}
class Rectangle implements Shape {
	int length,breadth;
	int area,perimeter;
	Rectangle()
	{
		this.length=length;
		this.breadth=breadth;
	}
	public void area() {
		area=length*breadth;
	}
	public void perimeter() {
		perimeter=2*(length+breadth);
	}
	
}
